def is_biallelic_snp(rec):
    alts = rec.alts or ()
    if len(alts) != 1:
        return False
    if len(rec.ref) != 1:
        return False
    if len(alts[0]) != 1:
        return False
    return True

def get_af(rec):
    af = rec.info.get("AF")
    if af is None:
        return None
    if isinstance(af, (tuple, list)):
        if len(af) == 0:
            return None
        af = af[0]
    try:
        af = float(af)
    except:
        return None
    return af

def ibs_category(gt_a, gt_b):
    if gt_a is None:
        return None
    if gt_b is None:
        return None
    if None in gt_a:
        return None
    if None in gt_b:
        return None
    a = tuple(sorted(gt_a))
    b = tuple(sorted(gt_b))
    if a == b:
        return 2
    if a == (0, 0) and b == (1, 1):
        return 0
    if a == (1, 1) and b == (0, 0):
        return 0
    return 1

def emission_probs_smoothed(p):
    q = 1.0 - p
    p0_0 = 2.0 * (p * p) * (q * q)
    p2_0 = (q ** 4) + 4.0 * (p * p) * (q * q) + (p ** 4)
    p1_0 = 1.0 - p0_0 - p2_0
    ibd0 = np.array([p0_0, p1_0, p2_0], dtype=float)
    ibd1 = np.array([SMOOTH, 2.0 * p * q, p * p + q * q], dtype=float)
    ibd1 = ibd1 / ibd1.sum()
    ibd2 = np.array([SMOOTH, SMOOTH, 1.0], dtype=float)
    ibd2 = ibd2 / ibd2.sum()
    return ibd0, ibd1, ibd2

