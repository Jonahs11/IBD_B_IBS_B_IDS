def window_smooth(states, win=10, thr0=0.50, thr12=0.80):
    s = np.asarray(states, dtype=int)
    out = s.copy()
    n = len(s)
    for i in range(0, n - win + 1):
        w = s[i:i+win]
        f0 = np.mean(w == 0)
        f1 = np.mean(w == 1)
        f2 = np.mean(w == 2)
        if f0 > thr0:
            out[i:i+win] = 0
            continue
        if f1 >= thr12:
            out[i:i+win] = 1
            continue
        if f2 >= thr12:
            out[i:i+win] = 2
            continue
    return out

